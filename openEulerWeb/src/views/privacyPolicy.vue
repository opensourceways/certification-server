<script>
export default {
  data() {
    return {
      toSign: "",
    };
  },
  created() {
    this.toSign = this.$route.query.toSign;
  },
  methods: {
    goHere(name) {
      const urls = {
        'openEuler兼容性平台': 'https://certification.openeuler.org/#/',
        '个人信息收集清单': {name: 'personalInformation'},
        'openEuler兼容性平台Cookie政策': {name: 'cookieProtocols'},
        '第三方SDK列表': {name: ''},
        '基金会隐私政策': 'https://www.openatom.org/#/privacyPolicy/0.15906735691316976',
        '拒绝请求': 'https://www.huawei.com/cn/personal-data-request',
        '第三方共享信息清单': {name: ''},
        'Uniportal账号的隐私声明': 'https://uniportal.huawei.com/uniportal1/privacyPolicyInter.html'
      }
      if (urls[name] && urls[name].name) {
        const routeData = this.$router.resolve({name: urls[name].name})
        window.open(routeData.href, '_blank')
      } else {
        window.open(urls[name])
      }
    },
    handleDelSign() {
      this.axios
          .put("/user/cancelPrivacyAgreement")
          .then((response) => {
            if (response.data.code === 200) {
              this.$message.success("撤销成功！");
              this.toSign = "true";
            }
          })
          .catch((err) => {
            this.$message.error(err?.response?.data?.message);
          });
    },
    handleSign() {
      this.axios
          .put("/user/signPrivacyAgreement")
          .then((response) => {
            if (response.data.code === 200) {
              this.$router.push({
                path: "/redirectJump",
              });
              this.$message.success("签署成功！")
            }
          })
          .catch((err) => {
            this.$message.error(err?.response?.data?.message);
          });
    },
  },
};
</script>

